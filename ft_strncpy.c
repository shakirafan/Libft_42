char	*ft_strncpy(char *dest, const char *src, int n)
{
	int i;
	int a;

	a = 0;
	i = 0;
	while (i < n)
	{
		dest[i] = src[i];
		if (src[i] == '\0')
		{
			a = i;
			i++;
			while (i < n)
			{
				dest[i] = src[a];
				i++;
			}
		}
		i++;
	
	}
	return (dest);
}
